               <P>The student online feedback system is a web-based application that is developed to rate the performance of each lecturers in different department by the students at the end of each semester. This application is aimed at analysing the average performance of each lecturer based on the feedback gotten from students after the end of every semester.
                   This web-based application will help reduce the workload of manually managing student feedbacks, and keeps it from being tempered with for any erroneous reasons. It also ensures the protection of feedback privacy to avoid conflicts between lecturers and students.
                   There are three classes of users (actors) in this system; the administrator, the lecturer (faculty) and the student which have their respective profiles.
